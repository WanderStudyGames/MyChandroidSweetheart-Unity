using UnityEngine;

public class AnimationRandomizer : StateMachineBehaviour
{
    [SerializeField] private AnimatorOverrideController _animatorOverrideController;
    [SerializeField] private string _clipToOverride;
    [SerializeField] private AnimationClip[] _clips = new AnimationClip[0];
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        if (_clips.Length == 0) return;
        var newClip = _clips[Random.Range(0, _clips.Length)];
        _animatorOverrideController[_clipToOverride] = newClip;
    }

